// Quando falamos de variáveis temos 3 tipos delas

// > Boolean 
let ligado: boolean = false;
let desligado = true;

// > Number
let idade: number = 18;
let idade2 = 18;

// > String 
let nome: string = 'willian';
let nome2 = 'willian';

// Ao declarar essa variável e tipar ela, estamos limitando ela a apenas aquele tipo de tipagem que atribuímos a ela. 
// ex: se tipamos a variável nome com string, não se pode atribuir números a ela, apenas strings.

// Temos também as variáveis abrangentes 

// as variáveis abrangentes são 2 tipos 

// > Any 
let listaAleatoria: any = ['willian', 18, 29, 'Joaozinho', true]

// na variável any podemos declarar qualquer coisa nela que isso será aceito sem nenhum erro.

// > Void 
let semNada: void 

// na variavel void é um tipo de variável vazia, que não tem necessidade de retornar nada, somente executar a função que lhe foi declarada.
